/**
 * Generate context files for all supported IDEs
 */
export function generateAllIDEFiles(rootDir) {
  const info = buildConsolidatedContext(rootDir);

  const generated = [];

  // Claude Code — already handled by analyze command (CLAUDE.md + .claude/rules/)
  // Generate for all other IDEs:

  generateCursorRules(rootDir, info);
  generated.push('.cursorrules');

  generateCopilotInstructions(rootDir, info);
  generated.push('.github/copilot-instructions.md');

  generateWindsurfRules(rootDir, info);
  generated.push('.windsurfrules');

  generateClineRules(rootDir, info);
  generated.push('.clinerules');

  generateAiderConventions(rootDir, info);
  generated.push('CONVENTIONS.md');

  generateRooRules(rootDir, info);
  generated.push('.roomodes');

  return generated;
}
