import fs from 'fs';
import path from 'path';
import { readFileSafe } from './scanner.js';
import { writeFile } from './writer.js';

/**
 * Builds a single consolidated context document from all .claude/rules/ files
 */
function buildConsolidatedContext(rootDir) {
  const rulesDir = path.join(rootDir, '.claude', 'rules');
  const claudeMd = readFileSafe(path.join(rootDir, 'CLAUDE.md')) || '';

  const files = [
    'architecture.md',
    'stack.md',
    'modules.md',
    'models.md',
    'api.md',
    'conventions.md',
    'gotchas.md',
  ];

  let context = '';
  for (const file of files) {
    const content = readFileSafe(path.join(rulesDir, file));
    if (content) {
      context += content.trim() + '\n\n---\n\n';
    }
  }

  // Extract project summary from CLAUDE.md
  const projectName = claudeMd.match(/^# (.+?) — Claude Memory/m)?.[1] || 'Project';
  const description = claudeMd.match(/## What this project is\n(.+)/m)?.[1] || '';
  const stackLine = claudeMd.match(/\*\*Stack\*\*: (.+)/)?.[1] || '';
  const devCmd = claudeMd.match(/\*\*Dev\*\*: `(.+)`/)?.[1] || '';
  const testCmd = claudeMd.match(/\*\*Test\*\*: `(.+)`/)?.[1] || '';
  const buildCmd = claudeMd.match(/\*\*Build\*\*: `(.+)`/)?.[1] || '';

  return { context, projectName, description, stackLine, devCmd, testCmd, buildCmd };
}

/**
 * Generate .cursorrules for Cursor IDE
 */
function generateCursorRules(rootDir, info) {
  const content = `# Cursor Rules — ${info.projectName}
# Auto-generated by codebase-memory (https://github.com/RagavRida/codebase-memory)
# This file gives Cursor full project context so it never needs to re-scan your codebase.

## Project Overview
${info.description}

## Stack: ${info.stackLine}
- Dev: \`${info.devCmd}\`
- Test: \`${info.testCmd}\`
- Build: \`${info.buildCmd}\`

## Codebase Knowledge

${info.context}

## Instructions
You have full codebase knowledge from the context above.
Do NOT re-read source files to understand structure — use this context.
Follow the conventions and patterns described above.
If something seems outdated, flag it rather than guessing.
`;

  writeFile(path.join(rootDir, '.cursorrules'), content);
}

